
public class Player {
	/**
	 * ATTRIBUT
	 */
	private String pseudo;
	private String email;
	private int point;
	/**
	 * CONSTRUCTEURS
	 */
	public Player(){	
	}
	/**
	 * METHODES
	 */
	public String getPseudo() {
		return pseudo;
	}
	public void setPseudo(String pseudo) {
		this.pseudo = pseudo;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	
}
